export const getWakeLockVideoSentinel = async (): Promise<WakeLockSentinel> => {
  const video = document.createElement('video');
  video.setAttribute('title', 'no sleep');
  video.setAttribute('playsinline', '');

  const sourceWebm = document.createElement('source');
  sourceWebm.src = VIDEO_WEBM;
  sourceWebm.type = 'video/webm';
  video.appendChild(sourceWebm);

  const sourceMp4 = document.createElement('source');
  sourceMp4.src = VIDEO_WEBM;
  sourceMp4.type = 'video/mp4';
  video.appendChild(sourceMp4);

  await new Promise<void>((resolve) => {
    video.addEventListener('loadedmetadata', () => resolve());
  });

  const handleTimeupdate = () => {
    if (video.currentTime > 0.5) {
      video.currentTime = Math.random();
    }
  };

  if (video.duration <= 1) {
    // webm source
    video.setAttribute('loop', '');
  } else {
    // mp4 source
    video.addEventListener('timeupdate', handleTimeupdate);
  }

  video.play();

  let released = false;

  const handleVisibilityChange = () => {
    if (document.visibilityState !== 'visible') {
      sentinel.release();
    }
  };

  document.addEventListener('visibilitychange', handleVisibilityChange);
  document.addEventListener('fullscreenchange', handleVisibilityChange);

  const sentinel = new class extends EventTarget {
    public readonly type = 'screen';

    public async release() {
      if (released) return;
      video.pause();
      video.removeEventListener('timeupdate', handleTimeupdate);
      document.removeEventListener('visibilitychange', handleVisibilityChange);
      document.removeEventListener('fullscreenchange', handleVisibilityChange);
      released = true;
      this.dispatchEvent(new Event('release'));
      return undefined;
    }

    public get released(): boolean {
      return released;
    }

    public set onrelease(_value) {
      throw new Error('Not supported');
    }

    public get onrelease(): EventListener {
      throw new Error('Not supported');
    }
  };

  return sentinel;
};
